    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // TODO add your handling code here: input value in table and calculate total subtotal tax
        
       
        
        DefaultTableModel model = new DefaultTableModel();
        model = (DefaultTableModel)table1.getModel();
        
        model.addRow(new Object[]
        {
            txtcode.getText(),
            txtname.getText(),
            txtqun.getValue(),
            txtprice.getText(),
            txtamount.getText(),    
        });
        
         
        int sum=0;
        
        for(int i=0; i<table1.getRowCount(); i++)
        {
          sum= sum + Integer.parseInt(table1.getValueAt(i, 4).toString());
            
          subtotal.setText(Integer.toString(sum));   //put value of sum in subtotal
          int Subtotal= Integer.parseInt(subtotal.getText());  //get subtotal in variable
          
          double Discount= (double) (Subtotal * 0.20);  //calculating discount
          String resultdisc= String.format("%.2f", Discount);
          discount.setText(resultdisc);
          
          double Vat = (double) (Subtotal * 0.07);
          String resultvat= String.format("%.2f", Vat);
          vat.setText(resultvat);
          
          double Total= (double) (Subtotal - Discount + Vat); 
          String resulttotal= String.format("%.2f", Total);
          total.setText(resulttotal);
          
        
            //inserting valu of table into order database
        }
        try{
            
                SimpleDateFormat df= new SimpleDateFormat("yyyy-MM-dd");

                String Tpcod = txtcode.getText();
                String Tpname = txtname.getText();
                String Tqty = txtqun.getValue().toString();
                String Tprice = txtprice.getText();
                String Tamount = txtamount.getText();
                String Tdate=df.format(txtdate.getDate());


                Class.forName("com.mysql.jdbc.Driver");  // for database connection
                java.sql.Connection con = DriverManager.getConnection("jdbc:mysql://localhost/pinnacle","root","");
            
        
                java.sql.PreparedStatement pst = con.prepareStatement("INSERT INTO orders(date, pcode, item_name, quantity, price, amount) VALUES(?,?,?,?,?,?)");

                pst.setString(1, Tdate);
                pst.setString(2, Tpcod);
                pst.setString(3, Tpname);
                pst.setString(4, Tqty);
                pst.setString(5, Tprice);
                pst.setString(6, Tamount);

                pst.executeUpdate();
                
               

                }
                catch(ClassNotFoundException | SQLException e){
                    JOptionPane.showMessageDialog(this, "Error in code");
                }
        
    }//GEN-LAST:event_addActionPerformed

    private void txtcodeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtcodeKeyPressed
        // TODO add your handling code here: Get price and name from database searching by item code
        if(evt.getKeyCode() == KeyEvent.VK_ENTER)
        { 
            try{
                Class.forName("com.mysql.jdbc.Driver");  // for database connection
                java.sql.Connection con = DriverManager.getConnection("jdbc:mysql://localhost/pinnacle","root","");

                PreparedStatement pst = (PreparedStatement) con.prepareStatement("select * from menu where code=? ");
                int code= Integer.parseInt(txtcode.getText());
                pst.setInt(1, code);
                ResultSet rsl= pst.executeQuery();

                if(rsl.next()==false)
                {
                   JOptionPane.showMessageDialog(this, "Item not found");
                }

                else{
                    txtname.setText(rsl.getString("item_name"));
                    txtprice.setText(rsl.getString("price"));
                    
                    txtqun.setValue(1);
                    int Quantity =Integer.parseInt(txtqun.getValue().toString());
                    int Price =Integer.parseInt(txtprice.getText());
                    int count= Quantity*Price;
                    txtamount.setText(String.valueOf(count));

                    }
                }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(this, "Error in code");
            }
        }
    }//GEN-LAST:event_txtcodeKeyPressed

